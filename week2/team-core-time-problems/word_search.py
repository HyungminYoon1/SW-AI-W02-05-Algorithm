from collections import Counter

"""
모듈 설명
---------
이 코드는 2차원 문자 보드(board) 안에서,
주어진 단어(word)를 상하좌우 인접 이동만으로 만들 수 있는지 검사한다.

규칙:
1. 한 번 사용한 칸은 같은 경로에서 다시 사용할 수 없다.
2. 이동은 상, 하, 좌, 우 4방향만 가능하다.
3. 단어의 문자를 순서대로 따라가야 한다.

최적화 포인트:
1. 문자열을 매번 새로 만들지 않고, 현재 몇 번째 문자까지 맞췄는지(index)만 추적한다.
2. 방문 좌표를 list 대신 visited[y][x] boolean 배열로 관리해 O(1)로 검사한다.
3. 보드에 필요한 문자 개수가 부족하면 DFS를 시작하기 전에 바로 False를 반환한다.
4. 단어의 첫 글자보다 마지막 글자가 보드에서 더 희귀하다면 단어를 뒤집어
   시작점 후보 수를 줄인다.
"""


def is_text_included(board, word):
    """
    보드 안에 word를 만들 수 있으면 True, 아니면 False를 반환한다.

    매개변수:
    - board: 2차원 문자 리스트
      예) [
            ["A", "B", "C", "E"],
            ["S", "F", "C", "S"],
            ["A", "D", "E", "E"]
          ]
    - word: 찾고 싶은 문자열
      예) "ABFDE"

    반환값:
    - bool
    """

    # ------------------------------------------------------------
    # 1. 예외 입력 처리
    # ------------------------------------------------------------
    # 보드가 비어 있거나, 첫 행이 비어 있으면 탐색 자체가 불가능하다.
    if not board or not board[0]:
        return False

    # 빈 문자열은 문제 정의에 따라 다를 수 있다.
    # 여기서는 "찾을 문자열이 비어 있으면 만들 수 있다"고 처리할 수도 있지만,
    # 일반적인 word search 문제에서는 보통 빈 문자열 입력을 따로 주지 않는다.
    # 필요하면 True로 바꿀 수 있다.
    if not word:
        return True

    # 보드의 세로 길이(행 수), 가로 길이(열 수)
    height = len(board)
    width = len(board[0])

    # ------------------------------------------------------------
    # 2. 길이 기반 사전 검증
    # ------------------------------------------------------------
    # 단어 길이가 전체 칸 수보다 길면 물리적으로 만들 수 없다.
    if len(word) > width * height:
        return False

    # ------------------------------------------------------------
    # 3. 문자 개수 기반 사전 검증
    # ------------------------------------------------------------
    # 예:
    # board에 'Z'가 1개밖에 없는데 word에 'Z'가 3개 필요하면
    # DFS를 할 필요 없이 바로 False다.
    board_counter = Counter()
    for row in board:
        board_counter.update(row)

    word_counter = Counter(word)

    for ch, needed_count in word_counter.items():
        if board_counter[ch] < needed_count:
            return False

    # ------------------------------------------------------------
    # 4. 시작점 최적화
    # ------------------------------------------------------------
    # 단어의 첫 글자가 보드에 매우 많고 마지막 글자가 더 적다면,
    # 단어를 뒤집어서 더 희귀한 문자부터 시작하는 편이 탐색량을 줄일 수 있다.
    #
    # 예:
    # word = "ABFDE"
    # board에 'A'가 10개 있고 'E'가 2개라면
    # "EDFBA"로 뒤집어서 시작하는 것이 보통 더 유리하다.
    if board_counter[word[0]] > board_counter[word[-1]]:
        word = word[::-1]

    # ------------------------------------------------------------
    # 5. 방문 여부 기록 배열
    # ------------------------------------------------------------
    # visited[y][x] == True 이면 현재 DFS 경로에서 이미 사용한 칸이다.
    visited = [[False] * width for _ in range(height)]

    # ------------------------------------------------------------
    # 6. DFS(백트래킹) 정의
    # ------------------------------------------------------------
    def dfs(x, y, index):
        """
        현재 좌표 (x, y)에서 word[index]부터 끝까지 만들 수 있는지 검사한다.

        매개변수:
        - x: 현재 열 좌표
        - y: 현재 행 좌표
        - index: 현재 맞춰야 하는 word의 문자 위치

        반환값:
        - bool
        """

        # --------------------------------------------------------
        # 6-1. 현재 칸 문자가 지금 찾아야 하는 문자와 다르면 실패
        # --------------------------------------------------------
        if board[y][x] != word[index]:
            return False

        # --------------------------------------------------------
        # 6-2. 마지막 문자까지 일치했다면 성공
        # --------------------------------------------------------
        if index == len(word) - 1:
            return True

        # --------------------------------------------------------
        # 6-3. 현재 칸 방문 처리
        # --------------------------------------------------------
        visited[y][x] = True

        # --------------------------------------------------------
        # 6-4. 4방향 탐색
        # --------------------------------------------------------
        # 오른쪽, 왼쪽, 아래, 위 순서
        directions = (
            (1, 0),   # right
            (-1, 0),  # left
            (0, 1),   # down
            (0, -1),  # up
        )

        for dx, dy in directions:
            nx = x + dx
            ny = y + dy

            # 범위 안에 있으면서 아직 방문하지 않은 칸만 탐색
            if 0 <= nx < width and 0 <= ny < height and not visited[ny][nx]:
                if dfs(nx, ny, index + 1):
                    return True

        # --------------------------------------------------------
        # 6-5. 여기까지 왔다는 것은 현재 경로로는 실패했다는 뜻
        #      따라서 백트래킹을 위해 방문 취소
        # --------------------------------------------------------
        visited[y][x] = False
        return False

    # ------------------------------------------------------------
    # 7. 시작점 순회
    # ------------------------------------------------------------
    # word[0]와 같은 문자를 가진 칸들을 시작점 후보로 삼는다.
    for y in range(height):
        for x in range(width):
            if board[y][x] == word[0]:
                if dfs(x, y, 0):
                    return True

    # 모든 시작점이 실패하면 False
    return False


# ------------------------------------------------------------
# 사용 예시
# ------------------------------------------------------------
if __name__ == "__main__":
    example_board = [
        ["A", "B", "C", "E"],
        ["S", "F", "C", "S"],
        ["A", "D", "E", "E"]
    ]
    example_word = "ABFDE"

    result = is_text_included(example_board, example_word)
    print(result)   # True